function [x0,y]=generateLSSParameter(H)
i=1:160;
u1=sum(H(1:40).*2.^(40-i(1:40)));
u2=sum(H(41:80).*2.^(80-i(41:80)));
v1=sum(H(81:120).*2.^(120-i(81:120)));
v2=sum(H(121:160).*2.^(160-i(121:160)));
x0=u1/2^40;
y0=u2/2^40;
x1=mod((x0*u1*v1/2^40+x0),1);
y1=mod((y0*u1*v1/2^40+y0),4);
x2=mod((x1*u2*v2/2^40+x1),1);
y2=mod((y1*u2*v2/2^40+y1),4);
x0=x2;
y=4-y2;